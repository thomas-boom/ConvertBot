import SwiftUI

enum UIConstants {
    // Outer container padding around the main card
    static let outerPadding: CGFloat = 24

    // Horizontal padding applied inside the content card
    static let contentHorizontalPadding: CGFloat = 40

    // Padding used inside grouped control areas
    static let innerHorizontalPadding: CGFloat = 18
    static let groupVerticalPadding: CGFloat = 16

    // Spacing rhythm choices
    static let smallSpacing: CGFloat = 12
    static let mediumSpacing: CGFloat = 18

    // Corner radii
    static let contentCornerRadius: CGFloat = 20
    static let smallCornerRadius: CGFloat = 10

    // Control sizing
    static let controlHeight: CGFloat = 32
    static let progressBarHeight: CGFloat = 8

    // Status bar padding
    static let statusBarHorizontalPadding: CGFloat = 20
    static let statusBarVerticalPadding: CGFloat = 10
}
